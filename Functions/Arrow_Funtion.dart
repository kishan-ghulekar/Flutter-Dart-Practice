void fun(int x,int y) => print(x+y);

int run(int x,int y) => x+y;
void main(){

	fun(10,20);

	print(run(50,60));
}

/*
┌──────────────────────────────────────────────────────────────┐
│ Step 1: fun()                                               │
├──────────────────────────────────────────────────────────────┤
│ void fun(int x, int y) => print(x + y);                     │
│                                                              │
│ • Arrow function (=>).                                      │
│ • Takes two integers: x and y.                              │
│ • Prints their sum.                                         │
│ • Return type is void (returns nothing).                    │
└──────────────────────────────────────────────────────────────┘

┌──────────────────────────────────────────────────────────────┐
│ Step 2: run()                                               │
├──────────────────────────────────────────────────────────────┤
│ int run(int x, int y) => x + y;                             │
│                                                              │
│ • Arrow function (=>).                                      │
│ • Takes two integers: x and y.                              │
│ • Returns the sum as an integer.                            │
└──────────────────────────────────────────────────────────────┘

┌──────────────────────────────────────────────────────────────┐
│ Step 3: fun(10, 20)                                         │
├──────────────────────────────────────────────────────────────┤
│ x = 10                                                      │
│ y = 20                                                      │
│                                                              │
│ Calculation: 10 + 20 = 30                                   │
│                                                              │
│ Output: 30                                                  │
└──────────────────────────────────────────────────────────────┘

┌──────────────────────────────────────────────────────────────┐
│ Step 4: run(50, 60)                                         │
├──────────────────────────────────────────────────────────────┤
│ x = 50                                                      │
│ y = 60                                                      │
│                                                              │
│ Calculation: 50 + 60 = 110                                  │
│                                                              │
│ Returns: 110                                                │
│ print() displays the returned value.                        │
└──────────────────────────────────────────────────────────────┘

┌──────────────────────────────────────────────────────────────┐
│                      Program Output                          │
├──────────────────────────────────────────────────────────────┤
│ 30                                                          │
│ 110                                                         │
└──────────────────────────────────────────────────────────────┘

┌──────────────────────────────────────────────────────────────┐
│                    Important Points                         │
├──────────────────────────────────────────────────────────────┤
│ ✔ The => operator is called the Arrow Function syntax.      │
│ ✔ It is used for single-expression functions.               │
│ ✔ void fun() prints the result directly.                    │
│ ✔ int run() returns the result, which can be printed later. │
└──────────────────────────────────────────────────────────────┘
